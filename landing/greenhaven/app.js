function normalizeLanguage(language) {
  return language?.toLowerCase().split('-')[0] ?? '';
}

function isSupportedLanguage(language) {
  return supportedLanguages.includes(normalizeLanguage(language));
}

function getStoredLanguage() {
  try {
    return window.localStorage.getItem(languageStorageKey);
  } catch {
    return null;
  }
}

function detectInitialLanguage() {
  const queryLanguage = new URLSearchParams(window.location.search).get('lang');
  if (isSupportedLanguage(queryLanguage)) return normalizeLanguage(queryLanguage);

  const storedLanguage = getStoredLanguage();
  if (isSupportedLanguage(storedLanguage)) return normalizeLanguage(storedLanguage);

  const browserLanguage = normalizeLanguage(window.navigator.language);
  if (isSupportedLanguage(browserLanguage)) return browserLanguage;

  return 'en';
}

function translate(key) {
  return translations[currentLanguage]?.[key] ?? translations.en[key] ?? '';
}

function applyTextTranslations() {
  document.querySelectorAll('[data-i18n]').forEach((node) => {
    const value = translate(node.dataset.i18n);
    if (value) node.textContent = value;
  });
}

function applyAttributeTranslations() {
  document.querySelectorAll('[data-i18n-attr]').forEach((node) => {
    const mappings = node.dataset.i18nAttr.split(';');

    mappings.forEach((mapping) => {
      const separatorIndex = mapping.indexOf(':');
      if (separatorIndex === -1) return;

      const attribute = mapping.slice(0, separatorIndex).trim();
      const key = mapping.slice(separatorIndex + 1).trim();
      const value = translate(key);
      if (attribute && value) node.setAttribute(attribute, value);
    });
  });
}

