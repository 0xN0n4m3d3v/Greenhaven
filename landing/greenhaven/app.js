function normalizeInvestorBriefText(value) {
  return value.replace(/\s+/g, ' ').trim();
}

function captureIntegratedInvestorTextNodes() {
  document.querySelectorAll('[data-integrated-investor]').forEach((root) => {
    const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
      acceptNode(node) {
        const parent = node.parentElement;
        const tag = parent?.tagName;
        if (tag === 'STYLE' || tag === 'SCRIPT') {
          return NodeFilter.FILTER_REJECT;
        }

        return normalizeInvestorBriefText(node.nodeValue)
          ? NodeFilter.FILTER_ACCEPT
          : NodeFilter.FILTER_REJECT;
      },
    });

    while (walker.nextNode()) {
      const node = walker.currentNode;
      integratedInvestorTextNodes.push({
        node,
        key: normalizeInvestorBriefText(node.nodeValue),
        leading: node.nodeValue.match(/^\s*/)?.[0] ?? '',
        trailing: node.nodeValue.match(/\s*$/)?.[0] ?? '',
      });
    }
  });
}

function applyIntegratedInvestorTranslations() {
  const config = window.greenhavenInvestorTranslations?.[currentLanguage];
  if (!config) return;

  integratedInvestorTextNodes.forEach((entry) => {
    const translated =
      currentLanguage === 'en' ? entry.key : config.text.get(entry.key);
    if (translated) {
      entry.node.nodeValue = entry.leading + translated + entry.trailing;
    }
  });
}

function persistLanguage(language) {
  try {
    window.localStorage.setItem(languageStorageKey, language);
  } catch {
    // The page still works if storage is unavailable.
  }
}

function updateLanguageUrl(language) {
  const url = new URL(window.location.href);
  url.searchParams.set('lang', language);
  window.history.replaceState(null, '', url);
}

function redirectLegacyInvestorPath() {
  if (!window.location.pathname.endsWith('/investors.html')) return;

  const url = new URL(window.location.href);
  url.pathname = '/';
  url.searchParams.set('lang', currentLanguage);
  url.hash = 'investors';
  window.history.replaceState(null, '', url);
}

function alignInitialHashTarget() {
  if (!window.location.hash) return;

  const targetId = decodeURIComponent(window.location.hash.slice(1));
  const target = document.getElementById(targetId);
  if (!target) return;

  requestAnimationFrame(() => {
    requestAnimationFrame(() => {
      const headerOffset =
        document.querySelector('[data-header]')?.getBoundingClientRect()
          .height ?? 0;
      const targetTop = target.getBoundingClientRect().top + window.scrollY;
      const scrollTop = Math.max(0, targetTop - headerOffset - 4);
      document.documentElement.scrollTop = scrollTop;
      document.body.scrollTop = scrollTop;
    });
  });
}

function applyI18n() {
  document.documentElement.lang = currentLanguage;
  document.title = translate('meta.title');
  applyTextTranslations();
  applyIntegratedInvestorTranslations();
  applyAttributeTranslations();
  updateLanguageButtons();
  updateInvestorLinks();
  applyWorldState();
}

