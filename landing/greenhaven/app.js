function getWorldStates() {
  return [
    {
      title: translate('world.square.title'),
      image: 'assets/town-square.png',
      button: translate('world.square.button'),
      copy: translate('world.square.copy'),
    },
    {
      title: translate('world.market.title'),
      image: 'assets/thiefs-market.png',
      button: translate('world.market.button'),
      copy: translate('world.market.copy'),
    },
  ];
}

let worldIndex = 0;

function applyWorldState() {
  const next = getWorldStates()[worldIndex];

  if (worldImage) worldImage.src = next.image;
  if (worldTitle) worldTitle.textContent = next.title;
  if (worldCopy) worldCopy.textContent = next.copy;
  if (worldToggle) worldToggle.textContent = next.button;
}

worldToggle?.addEventListener('click', () => {
  worldIndex = (worldIndex + 1) % getWorldStates().length;

  imageStage?.classList.add('switching');
  window.setTimeout(() => {
    applyWorldState();
    imageStage?.classList.remove('switching');
  }, 180);
});

