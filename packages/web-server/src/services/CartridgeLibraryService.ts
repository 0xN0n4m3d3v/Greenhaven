// FEAT-CART-LIB-1 — read-only cartridge library service.
//
// Backs the `Worlds & Heroes` GUI by surfacing installed cartridges,
// created heroes, and per-hero per-cartridge playthrough state in
// UI-friendly DTOs. This slice never writes; later slices own
// import preview/apply, reimport, and hero+cartridge launch.
//
// Privacy / boundary rules pinned here:
//
//   * `recovery_code_hash` and `recovery_code_prefix` never leave
//     the service. Hero summaries carry the player's public id +
//     display name + level/xp only.
//   * `entities.profile` is read for `created`/`summary` only.
//     Hidden runtime overlays, private NPC memory, and actor
//     statuses are out of scope here — the player is the
//     authoritative scope for this surface.
//   * Counts in `CartridgeSummary` come from explicit kind filters
//     against `entities` joined with `cartridge_records`; debug
//     dumps / raw payloads stay server-internal.

