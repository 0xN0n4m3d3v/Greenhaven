  /**
   * Permanently remove a hero identity and its local session history.
   *
   * This is a local single-operator library mutation, not a gameplay
   * turn. The player entity is the root; FK cascades remove player
   * progression/state tables and hero_cartridge_states. Sessions are
   * deleted explicitly first so transcript/outbox rows cascade too.
   */
  static async deleteHero(playerId: number): Promise<DeleteHeroResult | null> {
    return await withTransaction(async (client) => {
      const exists = await client.query<{id: number}>(
        `SELECT e.id
           FROM entities e
           JOIN players p ON p.entity_id = e.id
          WHERE e.id = $1
            AND e.kind = 'player'
          LIMIT 1`,
        [playerId],
      );
      if (!exists.rows[0]) return null;

      const sessionRows = await client.query<{id: string}>(
        `SELECT id
           FROM sessions
          WHERE player_id = $1`,
        [playerId],
      );
      const sessionsDeleted = await deleteSessions(
        client,
        sessionRows.rows.map((row) => row.id),
      );

      await client.query(
        `DELETE FROM entities
          WHERE id = $1
            AND kind = 'player'`,
        [playerId],
      );
      return {deleted: true as const, playerId, sessionsDeleted};
    });
  }

  /**
   * Reset every hero run in one installed world while preserving the
   * cartridge and its imported static content. After this operation a
   * hero can select the world again and start from the installed
   * cache without reimporting.
   */
  static async resetCartridge(
    cartridgeId: string,
  ): Promise<ResetCartridgeResult | null> {
    return await withTransaction(async (client) => {
      const exists = await client.query<{id: string}>(
        `SELECT id FROM cartridges WHERE id = $1 LIMIT 1`,
        [cartridgeId],
      );
      if (!exists.rows[0]) return null;

      const stateRows = await client.query<{
        player_id: number;
        last_session_id: string | null;
      }>(
        `SELECT player_id, last_session_id
           FROM hero_cartridge_states
          WHERE cartridge_id = $1`,
        [cartridgeId],
      );
      const playerIds = stateRows.rows.map((row) => row.player_id);
      const sessionIds = stateRows.rows
        .map((row) => row.last_session_id)
        .filter((id): id is string => typeof id === 'string' && id.length > 0);

      if (playerIds.length > 0) {
        await client.query(
          `UPDATE players
              SET current_location_id = NULL,
                  current_scene_id = NULL,
                  dialogue_partner_id = NULL
            WHERE entity_id = ANY($1::bigint[])`,
          [playerIds],
        );
      }
      const sessionsDeleted = await deleteSessions(client, sessionIds);
      const stateDelete = await client.query(
        `DELETE FROM hero_cartridge_states
          WHERE cartridge_id = $1`,
        [cartridgeId],
      );

      return {
        reset: true as const,
        cartridgeId,
        sessionsDeleted,
        playthroughStatesDeleted: stateDelete.rowCount ?? 0,
      };
    });
  }

  /**
   * Permanently remove an installed world. This deletes the cartridge
   * registry row, cartridge-scoped metadata/import/cache rows, all
   * playthrough state for that cartridge, and the non-player static
   * entities imported for the cartridge.
   */
  static async deleteCartridge(
    cartridgeId: string,
  ): Promise<DeleteCartridgeResult | null> {
    const result = await withTransaction(async (client) => {
      const exists = await client.query<{id: string}>(
        `SELECT id FROM cartridges WHERE id = $1 LIMIT 1`,
        [cartridgeId],
      );
      if (!exists.rows[0]) return null;

      const stateRows = await client.query<{last_session_id: string | null}>(
        `SELECT last_session_id
           FROM hero_cartridge_states
          WHERE cartridge_id = $1
            AND last_session_id IS NOT NULL`,
        [cartridgeId],
      );
      const sessionsDeleted = await deleteSessions(
        client,
        stateRows.rows
          .map((row) => row.last_session_id)
          .filter((id): id is string => typeof id === 'string' && id.length > 0),
      );

      const playthroughDelete = await client.query(
        `DELETE FROM hero_cartridge_states
          WHERE cartridge_id = $1`,
        [cartridgeId],
      );

      const entityRows = await client.query<{id: number}>(
        `SELECT id
           FROM entities
          WHERE cartridge_id = $1
            AND kind <> 'player'
            AND COALESCE(dynamic_origin, false) = false`,
        [cartridgeId],
      );
      const entityIds = entityRows.rows.map((row) => row.id);
      await clearPlayerEntityReferences(client, entityIds);
      const entityDelete =
        entityIds.length > 0
          ? await client.query(
              `DELETE FROM entities
                WHERE id = ANY($1::bigint[])`,
              [entityIds],
            )
          : {rowCount: 0};

      await client.query(
        `DELETE FROM cartridges
          WHERE id = $1`,
        [cartridgeId],
      );

      const next = await client.query<{id: string}>(
        `SELECT id
           FROM cartridges
          ORDER BY installed_at ASC, id ASC
          LIMIT 1`,
      );
      const nextDefaultCartridgeId = next.rows[0]?.id ?? null;
      if (nextDefaultCartridgeId) {
        await client.query(
          `INSERT INTO cartridge_meta (key, value, description, updated_at)
           SELECT key, value, description, now()
             FROM cartridge_meta_scoped
            WHERE cartridge_id = $1
           UNION ALL
           SELECT 'cartridge_id', to_jsonb($1::text), 'Identifier of the active cartridge.', now()
            WHERE NOT EXISTS (
              SELECT 1
                FROM cartridge_meta_scoped
               WHERE cartridge_id = $1
                 AND key = 'cartridge_id'
            )
           ON CONFLICT (key) DO UPDATE
             SET value = EXCLUDED.value,
                 description = EXCLUDED.description,
                 updated_at = now()`,
          [nextDefaultCartridgeId],
        );
      } else {
        await client.query(
          `DELETE FROM cartridge_meta`,
        );
      }

      return {
        deleted: true as const,
        cartridgeId,
        entitiesDeleted: entityDelete.rowCount ?? 0,
        sessionsDeleted,
        playthroughStatesDeleted: playthroughDelete.rowCount ?? 0,
        nextDefaultCartridgeId,
      };
    });
    if (result) clearMetaCache();
    return result;
  }
