export function validateUiMessageCatalog(): void {
  validateTranslationCoverage(
    BASE_MESSAGES['en'] ?? {},
    UI_MESSAGES,
    SUPPORTED_LANGUAGES,
  );

  const expectedLanguages = new Set(
    SUPPORTED_LANGUAGES.map((language) => language.code),
  );
  const extraLanguages = Object.keys(UI_MESSAGES).filter(
    (language) => !expectedLanguages.has(language),
  );
  if (extraLanguages.length > 0) {
    throw new Error(
      'translation catalog has unsupported languages: ' +
        extraLanguages.join(', '),
    );
  }

  const baseKeys = new Set(Object.keys(BASE_MESSAGES['en'] ?? {}));
  for (const language of SUPPORTED_LANGUAGES) {
    const messages = UI_MESSAGES[language.code] ?? {};
    const extraKeys = Object.keys(messages).filter((key) => !baseKeys.has(key));
    if (extraKeys.length > 0) {
      throw new Error(
        'translation catalog ' +
          language.code +
          ' has extra keys: ' +
          extraKeys.join(', '),
      );
    }
    const emptyKeys = Object.entries(messages)
      .filter(([, value]) => typeof value !== 'string' || value.trim() === '')
      .map(([key]) => key);
    if (emptyKeys.length > 0) {
      throw new Error(
        'translation catalog ' +
          language.code +
          ' has empty keys: ' +
          emptyKeys.join(', '),
      );
    }
    const unresolvedKeys = Object.entries(messages)
      .filter(([key, value]) => value === key)
      .map(([key]) => key);
    if (unresolvedKeys.length > 0) {
      throw new Error(
        'translation catalog ' +
          language.code +
          ' has unresolved key values: ' +
          unresolvedKeys.join(', '),
      );
    }
  }
}

validateUiMessageCatalog();

export function getUiTranslations(locale: string): Record<string, string> {
  const normalized = normalizeSupportedLanguageCode(locale) ?? 'en';
  const base = UI_MESSAGES['en'] ?? {};
  return repairMojibakeRecord({ ...base, ...(UI_MESSAGES[normalized] ?? {}) });
}
