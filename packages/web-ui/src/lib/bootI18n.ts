function validateBootTextCoverage(): void {
  const keys = Object.keys(EN_BOOT_TEXT) as BaseBootTextKey[];
  const deepSeekKeys = Object.keys(EN_DEEPSEEK_TEXT) as DeepSeekBootTextKey[];
  for (const language of SUPPORTED_LANGUAGES) {
    const text = BOOT_TEXT[language.code];
    if (!text) {
      throw new Error(`missing boot menu translation for ${language.code}`);
    }
    const missing = keys.filter(key => !text[key]);
    if (missing.length > 0) {
      throw new Error(
        `boot menu translation ${language.code} is missing keys: ${missing.join(', ')}`,
      );
    }
    const deepSeekText = DEEPSEEK_BOOT_TEXT[language.code];
    if (!deepSeekText) {
      throw new Error(`missing DeepSeek settings translation for ${language.code}`);
    }
    const deepSeekMissing = deepSeekKeys.filter(key => !deepSeekText[key]);
    if (deepSeekMissing.length > 0) {
      throw new Error(
        `DeepSeek settings translation ${language.code} is missing keys: ${deepSeekMissing.join(', ')}`,
      );
    }
  }
}

validateBootTextCoverage();

export function bootTextForLanguage(language: string | null | undefined): BootText {
  const normalized = normalizeSupportedLanguageCode(language) ?? 'en';
  return repairMojibakeRecord({
    ...EN_BOOT_TEXT,
    ...EN_DEEPSEEK_TEXT,
    ...(BOOT_TEXT[normalized] ?? EN_BOOT_TEXT),
    ...(DEEPSEEK_BOOT_TEXT[normalized] ?? EN_DEEPSEEK_TEXT),
  });
}
